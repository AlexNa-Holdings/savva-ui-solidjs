// A generic wrapper for toolbar buttons
export function ToolbarButton(props) {
  return (
    <button
      type="button"
      onClick={props.onClick}
      title={props.title}
      class="p-2 rounded-md hover:bg-[hsl(var(--accent))]"
      aria-label={props.title}
    >
      {props.children}
    </button>
  );
}

